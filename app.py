@st.cache_data(show_spinner=False)
def load_data(path: str = "NR_dataset.xlsx") -> pd.DataFrame:
    """Read the Excel dataset and standardize types."""
    df = pd.read_excel(path, engine="openpyxl")
    # Keep only relevant columns (if extra columns exist)
    cols_present = [c for c in REQUIRED_COLS if c in df.columns]
    missing = [c for c in REQUIRED_COLS if c not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns in Excel: {missing}")

    df = df[REQUIRED_COLS].copy()

    # Types
    df["CustomerID"] = df["CustomerID"].apply(_safe_str)
    df["TransactionID"] = df["TransactionID"].apply(_safe_str)

    # Dates
    df["TransactionDate"] = pd.to_datetime(df["TransactionDate"], errors="coerce")
    # Drop rows with invalid dates (cannot be analyzed in time-series)
    df = df.dropna(subset=["TransactionDate"]).copy()

    # Numeric sanitization
    df["PurchaseAmount"] = pd.to_numeric(df["PurchaseAmount"], errors="coerce").fillna(0.0)
    df["PurchaseAmount"] = df["PurchaseAmount"].clip(lower=0)

    df["CustomerSatisfaction"] = pd.to_numeric(df["CustomerSatisfaction"], errors="coerce")
    # Keep satisfaction in 1..5 if possible; otherwise NaN
    df.loc[~df["CustomerSatisfaction"].between(1, 5, inclusive="both"), "CustomerSatisfaction"] = np.nan

    # Categorical fill
    cat_cols = [
        "ProductCategory",
        "CustomerAgeGroup",
        "CustomerGender",
        "CustomerRegion",
        "RetailChannel",
        "label",
    ]
    for c in cat_cols:
        df[c] = df[c].astype("string").fillna("Unknown")

    # Derived: YearMonth (period) for stable grouping
    df["YearMonth"] = df["TransactionDate"].dt.to_period("M").astype(str)

    # Normalize label order (keep original text, but encourage canonical)
    df["label"] = df["label"].astype(str)
    return df


def add_customer_features(df: pd.DataFrame) -> pd.DataFrame:
    """
    Add per-customer features that are used across visuals:
    - CustomerLifetimeRevenue: sum PurchaseAmount per CustomerID (within current filtered window)
    - PurchaseFrequency: transaction count per customer (within current filtered window)
    - LastTransactionDate: most recent transaction date per customer
    """
    g = df.groupby("CustomerID", as_index=False)
    cust = g.agg(
        CustomerLifetimeRevenue=("PurchaseAmount", "sum"),
        PurchaseFrequency=("TransactionID", "nunique"),
        LastTransactionDate=("TransactionDate", "max"),
        Segment=("label", lambda s: s.iloc[-1]),
        Region=("CustomerRegion", lambda s: s.iloc[-1]),
        AvgSatisfaction=("CustomerSatisfaction", "mean"),
    )
    cust["LastTransactionDate"] = cust["LastTransactionDate"].dt.date.astype(str)
    return cust


