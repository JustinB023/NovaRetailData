def _fmt_currency(x: float) -> str:
    try:
        return f"${x:,.0f}"
    except Exception:
        return str(x)


def _safe_str(x) -> str:
    if pd.isna(x):
        return ""
    return str(x)


def _minmax_01(s: pd.Series) -> pd.Series:
    """Min-max normalize to [0, 1]. If constant, return zeros."""
    s = s.astype(float)
    mn, mx = s.min(), s.max()
    if pd.isna(mn) or pd.isna(mx) or mx == mn:
        return pd.Series(np.zeros(len(s)), index=s.index)
    return (s - mn) / (mx - mn)


def _to_png_bytes(fig) -> bytes:
    """
    Export Plotly figure to PNG bytes (requires kaleido).
    Falls back to HTML bytes if PNG export fails.
    """
    try:
        return pio.to_image(fig, format="png", scale=2)
    except Exception:
        # Fallback: downloadable HTML snapshot (still useful offline)
        html = pio.to_html(fig, include_plotlyjs="cdn")
        return html.encode("utf-8")


